// Рекурсивная функция для нахождения суммы элементов вложенного массива:
function sumArray($array) {
    $sum = 0;
    foreach ($array as $element) {
        if (is_array($element)) {
            $sum += sumArray($element); // рекурсивный вызов для обхода вложенных массивов
        } else {
            $sum += $element;
        }
    }
    return $sum;
}


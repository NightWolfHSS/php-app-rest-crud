// пояснение
// Рекурсия - это процесс, при котором функция вызывает саму себя в своем собственном теле. В рекурсивной функции происходит повторное выполнение кода с новыми значениями, пока не будет достигнуто определенное условие остановки.

// Основная идея рекурсии состоит в разделении задачи на более простые подзадачи и решении их по отдельности. Каждый раз, когда функция вызывает саму себя, она решает более простую версию задачи, пока не достигнет базового случая, когда рекурсия прекращается.


// factorial
function factorial($n)
{
	if ( $n <= 1 ) { return 1; }
	else { return $n * factorial($n - 1); }
}

// echo factorial(10);

// fibonachi
function fibonachi($n)
{
	if ($n <= 1) {
		return $n;
	} else {
		//рекурсивный случай: число Фибоначчи N равно сумме двух предыдущих чисел Фибоначчи
		return fibonachi($n - 1) + fibonachi($n - 2); 
	}
}
// Выведет 8 (последовательность Фибоначчи: 0, 1, 1, 2, 3, 5, 8)
